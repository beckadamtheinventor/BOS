	jq peek_main
	db "FEX",0
peek_main:
	ld hl,bos.return_code_flags
	set bos.bReturnHex,(hl)
	set bos.bReturnNotError,(hl)
	pop bc
	ex (sp),hl
	push bc
	ld a,(hl)
	ld c,1
	cp a,'b'
	jq z,.preinchl
	inc c
	cp a,'s'
	jq z,.preinchl
	inc c
	cp a,'i'
	jq z,.preinchl
	inc c
	cp a,'l'
	jq z,.preinchl
	dec hl
	ld c,1
.preinchl:
	push bc
	inc hl
	push hl
	call osrt.hexstr_to_int
	pop bc,bc
	ld a,c
	jq osrt.read_a_from_addr
